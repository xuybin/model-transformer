/*正则类型
** 1.用于辅助new StringField().regexp(ReType.idCard)效验
** 2.用于标识字段类型，以便在代码生成时根据Field.attributes.regexp[i]==RegExpType.idCard实现更具体的效验
**/
export const Type = {
  /** 身份证(只做最基本的效验，进一步效验请在代码生成的根据Field.attributes.regexp[i]==RegExpType.idCard实现)
      // 第一位不可能是0
      // 第二位到第六位可以是0-9
      // 第七位到第十位是年份，所以七八位为19或者20
      // 十一位和十二位是月份，这两位是01-12之间的数值
      // 十三位和十四位是日期，是从01-31之间的数值
      // 十五，十六，十七都是数字0-9
      // 十八位可能是数字0-9，也可能是X(大写)
    */
  idCard:
    /^[1-9][0-9]{5}([1][9][0-9]{2}|[2][0][0|1][0-9])([0][1-9]|[1][0|1|2])([0][1-9]|[1|2][0-9]|[3][0|1])[0-9]{3}([0-9]|[X])$/,
  /** 12位行政区划码 */
  addressCode: /^[1-8]{1}[0-7]{1}[0-9]{4,10}$/,
  /** 18位统一社会信用代码 */
  uniformSocialCreditCode: /^[^_IOZSVa-z\W]{2}\d{6}[^_IOZSVa-z\W]{10}$/,
  email:
    /^(([^<>()[\]\\.,;:\s@\"]+(\.[^<>()[\]\\.,;:\s@\"]+)*)|(\".+\"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  url:
    /((([A-Za-z]{3,9}:(?:\/\/)?)(?:[-;:&=\+\$,\w]+@)?[A-Za-z0-9.-]+|(?:www.|[-;:&=\+\$,\w]+@)[A-Za-z0-9.-]+)((?:\/[\+~%\/.\w-_]*)?\??(?:[-\+=&;%@.\w_]*)#?(?:[\w]*))?)/,
  ipAddress:
    /^(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$/,
  /** 11位手机号码 */
  mobileNo: /^1[0-9]{10}$/,
  //7或8位的座机号(可带分机号),如0857-88084888-011
  telephoneNo: /^((0\d{2,3})[-,—]?)(\d{7,8})([-,—](\d{3}))?$/,
  /** 日期,如:2019-07-23 */
  date:
    /^(?:(?!0000)[0-9]{4}-(?:(?:0[1-9]|1[0-2])-(?:0[1-9]|1[0-9]|2[0-8])|(?:0[13-9]|1[0-2])-(?:29|30)|(?:0[13578]|1[02])-31)|(?:[0-9]{2}(?:0[48]|[2468][048]|[13579][26])|(?:0[48]|[2468][048]|[13579][26])00)-02-29)$/,
  /** 日期,如:201907 */
  yearMonth:
    /^([0-9]{3}[1-9]|[0-9]{2}[1-9][0-9]{1}|[0-9]{1}[1-9][0-9]{2}|[1-9][0-9]{3})(0[1-9]{1}|1[0-2]{1})$/,
  /** 日期 月份,如:07 */
  month: /^0[1-9]$|^1[0-2]$/,
};
